def removeWhiteV1(line):
    newLine = ""
    reachedStart = False
    for char in line:
        if not reachedStart:
            if char == "░":
                newLine += " "
            else:
                newLine += char
    return newLine

def removeWhiteV2(line):
    newLine = ""
    reachedStart = False
    for char in line:
        if not reachedStart:
            if char == "░":
                newLine += " "
            else:
                newLine += char
                reachedStart = True
        else:
            newLine += char
    return newLine

def reverseSprite(sprite):
    return sprite[::-1]

def v1(sprite):
    newSprite = []
    for line in sprite:
        line = removeWhiteV1(line)
        newSprite.append(line)
    return newSprite

def v2(sprite):
    newSprite = []
    for line in sprite:
        line = removeWhiteV2(line)
        line = reverseSprite(line)
        line = removeWhiteV2(line)
        line = reverseSprite(line)
